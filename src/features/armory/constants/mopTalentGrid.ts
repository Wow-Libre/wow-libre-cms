export function hasMopTalentGrid(classId: number): boolean {
  const tiers = MOP_TALENT_GRID[classId];
  if (!tiers || tiers.length !== 6) return false;
  return tiers.every((row) => row.some((id) => id > 0));
}

export function mopTalentTierLevel(tierIndex: number): number {
  return MOP_TALENT_TIER_LEVELS[Math.max(0, Math.min(5, tierIndex))];
}
